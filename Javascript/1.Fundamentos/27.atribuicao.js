const a = 7
let b = 3

b += a                     // --> Atribuição aditiva. É o mesmo que b = b + a 
console.log(b)

b -= 4                    // --> Atribuição subtrativa. É o mesmo que b = b - 4
console.log(b)

b *= 2                    // --> Atribuição multiplicativa. É o mesmo que b = b * 2
console.log(b)

b /= 2                    // --> Atribuição divisiva. É o mesmo que b = b + a
console.log(b)

b %= 2                    // --> Atribuição modular. É o mesmo que b = b % 2
console.log(b)